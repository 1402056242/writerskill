#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import os
import sys
import re
import glob

def count_chinese_words(text):
    """统计中文字数（包含标点符号），排除空格、换行、英文字母、数字和 markdown 标记"""
    text = re.sub(r'\s+', '', text)
    text = re.sub(r'[a-zA-Z0-9]+', '', text)
    text = re.sub(r'[#*_`\[\]\(\)]+', '', text)
    return len(text)

def build_calibration_prompt(chapter_num):
    print(f"正在为【第{chapter_num}章】组装完稿校准提示词...")
    
    # 1. 读取本章草稿并统计字数
    draft_file = os.path.join("4-正文", f"第{chapter_num}章_草稿.md")
    if not os.path.exists(draft_file):
        print(f"❌ 错误：未找到草稿文件 {draft_file}")
        sys.exit(1)
        
    with open(draft_file, 'r', encoding='utf-8') as f:
        draft_content = f.read().strip()
        
    word_count = count_chinese_words(draft_content)
    
    # 2. 读取写作提示词（里面已经包含了章纲、文风、宪法等所有约束）
    prompt_file = os.path.join("4-正文", "prompts", f"第{chapter_num}章_写作提示词.md")
    if not os.path.exists(prompt_file):
        print(f"❌ 错误：未找到写作提示词文件 {prompt_file}，无法进行对照检查。")
        sys.exit(1)
        
    with open(prompt_file, 'r', encoding='utf-8') as f:
        writing_prompt_content = f.read().strip()

    # 3. 组装校准 Prompt
    prompt = f"# 第{chapter_num}章 完稿校准指令 (Calibration Prompt)\n\n"
    prompt += "> **任务目标**：你现在是一位极其严苛的网文主编，请根据之前的【写作提示词】要求，对刚刚生成的【本章草稿】进行七项严格审查，并输出【完稿自检卡】。\n\n"
    
    prompt += "## 一、审查材料\n\n"
    prompt += f"### 1. 本章草稿内容（当前中文字数：{word_count} 字）\n"
    prompt += "```markdown\n" + draft_content + "\n```\n\n"
    
    prompt += "### 2. 原始写作约束（作为评判标准）\n"
    prompt += "```markdown\n" + writing_prompt_content + "\n```\n\n"
    
    prompt += f"""---

## 二、七项严格审查标准

请逐一核对以下七项标准：

1. **字数检查**：当前中文字数为 {word_count} 字。是否达到最低 1800 字的要求？（若未达到，必须判定为不通过，并要求扩写细节）。

2. **大纲符合性检查**：对照原始约束中的【本章核心指令】，检查本章核心事件、场景和结尾钩子是否完全按计划执行？是否有遗漏场景？

3. **去AI味逐项检查**（对照项目宪法 §2 负向词库，逐条命中必须列出原句并给替换建议）：
   - **神态微动作**：是否出现「眼中闪过/瞳孔收缩/嘴角勾起/嘴角勾起一抹/深吸一口气/捏紧拳头/指节发白/浮现出一抹/脸上堆满了笑」等禁用词？逐处列出
   - **滥用比喻**：全章比喻句（含「像/宛如/仿佛/犹如/似乎」引导的比喻）是否超过 **3 个**？列出所有比喻句，超出者必须删减
   - **逻辑废话**：是否出现「他知道/他相信/不得不说/心中一动/心中一凛/这意味着/电光火石之间/说时迟那时快」？逐处列出
   - **对话腔调**：是否存在每个角色说话风格雷同（无身份化差异）？是否每句对话都接「说道/问道/笑道」标签（超过 40% 即为过密）？
   - **结尾升华**：章末是否有总结感慨、「他深知……」「这一刻他明白了……」类旁白？
   - **上帝解释腔**：是否出现作者跳出角色视角的解释旁白（「这意味着……」「他不知道的是……」「之所以…是因为」）？
   - **段落结构同构**：从结构角度扫描全章——是否连续 3 段以上都是「动作句 → 心理句 → 总结句」的三件套模板？是否每段长度、句数、功能都高度接近（机械均匀感）？是否每段末尾都补了一句抽象总结或感慨，而不是停在动作/对话/场景上？修法：改变信息进入方式，打破段落等厚，允许短段、中段、厚段按节奏交替；能停在具体事实或动作上就不要再概括。
   → **判定规则**：以上七个子项，任意一项有命中即为本项「不通过」，必须逐项给出修改方案后再整体通过

4. **接续检查**：对照原始约束中的【近期前文参考】，检查剧情、时间、地点是否无缝接续？是否存在逻辑断层？

5. **角色一致性检查**：主角性格是否贯穿本章？是否有 OOC（Out Of Character）行为？角色成长是否有足够的事件与心理铺垫？

6. **金手指合规性**：（若本章涉及）检查表现形式是否符合原始约束中的设定？

7. **叙事结构检查**（场景/段落层面）：
   - **开头吸引点**：前 300 字是否出现具体冲突、危险、欲望、秘密或异常？是否避开了大段世界观解释或人物履历？
   - **铺垫-过程-余波**：本章核心转折是否具备三段——触发原因（铺垫）、尝试+阻碍+代价（过程）、关系/状态残留（余波）？是否只写了结果，而读者没有经历压力？
   - **人物具体选择**：主角是否做了至少一次具体选择，而不是只被剧情推着走？是否有至少一次尝试、失败或代价？
   - **节奏落点**：每 300–500 字是否有情绪波动、压力变化或小转折？是否有必要的喘息段，而不是一路硬冲？
   - **章末钩子具体性**：章末是否用具体动作/信息/物品/人物出现收尾？是否避开了「真正的危机才刚刚开始」「命运的齿轮开始转动」等机械预告句？
   → **判定规则**：以上五个子项，任意一项不通过即为本项「不通过」，必须逐项指出位置并给出修改建议

---

## 三、输出要求

请直接输出一份**【完稿自检卡】**，格式如下：

### 📝 第{chapter_num}章 完稿自检卡

- **字数达标**：[通过/不通过]（当前字数：{word_count}）
- **大纲符合度**：[通过/不通过]（简述理由）
- **去AI味检查**：[通过/不通过]
  - 神态微动作：[通过/不通过]（列出命中原句）
  - 滥用比喻：[通过/不通过]（列出所有比喻句，标注数量）
  - 逻辑废话：[通过/不通过]（列出命中原句）
  - 对话腔调：[通过/不通过]（简述理由）
  - 结尾升华：[通过/不通过]（列出命中原句）
  - 上帝解释腔：[通过/不通过]（列出命中原句）
  - 段落结构同构：[通过/不通过]（列出连续同构段落的位置，说明重复的结构模式）
- **剧情接续**：[通过/不通过]（简述理由）
- **角色一致性**：[通过/不通过]（简述理由）
- **金手指合规**：[通过/不通过/不涉及]（简述理由）
- **叙事结构**：[通过/不通过]
  - 开头吸引点：[通过/不通过]（简述理由）
  - 铺垫-过程-余波：[通过/不通过]（指出缺失的环节）
  - 人物具体选择：[通过/不通过]（简述理由）
  - 节奏落点：[通过/不通过]（标出无落点的段落范围）
  - 章末钩子具体性：[通过/不通过]（列出原句）

**最终结论**：
- 如果七项全部通过：请回复「**✅ 审查通过。请将草稿重命名为正文，并进入下一步。**」
- 如果有任何一项不通过：请明确列出**必须修改的段落和具体修改建议**，并回复「**❌ 审查未通过。请根据以上建议对草稿进行修订。**」
"""

    os.makedirs("4-正文/prompts", exist_ok=True)
    output_file = os.path.join("4-正文", "prompts", f"第{chapter_num}章_校准提示词.md")
    try:
        with open(output_file, 'w', encoding='utf-8') as f:
            f.write(prompt)
        print(f"✅ 成功生成校准提示词文件：{output_file}")
        print(f"📊 本章当前中文字数：{word_count} 字")
    except Exception as e:
        print(f"❌ 生成校准提示词失败: {e}")

if __name__ == "__main__":
    if len(sys.argv) < 2:
        print("用法: python3 build_calibration_prompt.py <章号>")
        print("示例: python3 build_calibration_prompt.py 15")
        sys.exit(1)
        
    try:
        chap_num = int(sys.argv[1])
        build_calibration_prompt(chap_num)
    except ValueError:
        print("❌ 错误：章号必须是数字。")
        sys.exit(1)
